<h2 class="cover heading">Über Chefkoch</h2>
<img class="bild" src=https://img.chefkoch-cdn.de/img/default/social-sharing-default.jpg>
<p>Chefkoch.de ist ein Kochportal auf dem Nutzer ihre eigenen Rezepte Hochladen Können.
    Im Mittelpunkt stehen dabei eine Rezeptdatenbank mit über 330.000 von Nutzern eingestellten Rezepten
    sowie über 70 Foren rund um Kochen,
    Backen und alles, was die Nutzer bewegt.
    Chefkock ist die Tochtergesellschaft des Hamburger Verlaghauses Gruener + Jahr.

</p>

